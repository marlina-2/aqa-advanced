let circleRadius = 15;
let circleArea = Math.PI * Math.pow(circleRadius, 2);
circleArea = circleArea.toFixed(2);
console.log("Circle area is: ", circleArea);

let length = 13;
let width = 5
let rectangleArea = length * width;
rectangleArea = rectangleArea.toFixed(2);
console.log("Rectangle area is: ", rectangleArea)

let cylinderRadius = 4;
let height = 8;
let cylinderVolume = Math.PI * Math.pow(cylinderRadius, 2) * height;
cylinderVolume = cylinderVolume.toFixed(2);
console.log("Cylinder volume is: ", cylinderVolume);
